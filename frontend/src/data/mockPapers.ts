export const getStrandColor = (strand: string): string => {
  return "primary";
};

export const getFeaturedPapers = (): Paper[] => {
  return mockPapers.filter((paper) => paper.is_featured);
};

export const getPapersByStrand = (strand: string): Paper[] => {
  return mockPapers.filter((paper) => paper.strand === strand);
};

export const searchPapers = (query: string): Paper[] => {
  const lowercaseQuery = query.toLowerCase();
  return mockPapers.filter(
    (paper) =>
      paper.title.toLowerCase().includes(lowercaseQuery) ||
      paper.abstract.toLowerCase().includes(lowercaseQuery) ||
      paper.authors.some((author) =>
        author.toLowerCase().includes(lowercaseQuery)
      ) ||
      paper.keywords.some((keyword) =>
        keyword.toLowerCase().includes(lowercaseQuery)
      ) ||
      paper.strand.toLowerCase().includes(lowercaseQuery)
  );
};
